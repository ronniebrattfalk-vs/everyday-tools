function lazyTool(fileName, exportName) {
  return lazy(() => {
    const loader = toolModules[`../tools/${fileName}`]
    if (!loader) throw new Error(`Missing tool module: ${fileName}`)
    return loader().then((module) => ({ default: module[exportName] }))
  })
}

export const registeredTools = tools.map((tool) => ({
  ...tool,
  component: lazyTool(tool.component.file, tool.component.exportName),
  iconComponent: iconMap[tool.icon] ?? FileText,
}))
